export function Suma(num1:number,num2:number): number {
    return num1 + num2
}

export function Resta(num1:number,num2:number): number{
    return num1 - num2
}

export function Mult(num1:number, num2:number): number{
    return num1 * num2
}

export function Div(num1:number, num2:number):number{
    return num1 / num2
}